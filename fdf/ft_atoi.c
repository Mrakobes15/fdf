#include "fdf.h"

int	ft_atoi(const char *str)
{
	long			res;
	long			minus;
	unsigned int	i;

	res = 0;
	minus = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i++] - '0';
		if (res > 2147483648 && minus == -1)
			return (0);
		if (res > 2147483647 && minus == 1)
			return (-1);
	}
	return ((int)(res * minus));
}
